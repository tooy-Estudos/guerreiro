export function mergeLuisPrumoState(rawState = {}) {
  const state = rawState || {};
  const projects = Array.isArray(state.projects) ? [...state.projects] : [];
  const existingIndex = projects.findIndex(isLuisPrumoProject);
  const existingProject = existingIndex >= 0 ? projects[existingIndex] : null;
  const targetProjectId = existingProject?.id || LUIS_PRUMO_PROJECT_ID;
  const baseProject = createLuisPrumoProject(targetProjectId);

  const mergedProject = {
    ...baseProject,
    ...(existingProject || {}),
    id: targetProjectId,
    name: existingProject?.name || baseProject.name,
    description: existingProject?.description || baseProject.description,
    emoji: existingProject?.emoji || baseProject.emoji,
    color: existingProject?.color || baseProject.color,
    objective: existingProject?.objective || baseProject.objective,
    target: existingProject?.target || baseProject.target,
    pilarId: existingProject?.pilarId || baseProject.pilarId,
    pilar: existingProject?.pilar || baseProject.pilar,
    defaultTimer: existingProject?.defaultTimer || baseProject.defaultTimer,
    programaNome: existingProject?.programaNome || baseProject.programaNome,
    dataInicio: existingProject?.dataInicio || baseProject.dataInicio,
    dataFim: existingProject?.dataFim || baseProject.dataFim,
    ativo: existingProject?.ativo === false ? false : true,
    roadmap: mergeById(existingProject?.roadmap || [], baseProject.roadmap),
    rotinas: mergeById(existingProject?.rotinas || [], LUIS_PRUMO_JULHO_ROTINAS),
    notes: mergeNotes(existingProject?.notes || [], [LUIS_PRUMO_JULHO_NOTE])
  };

  if (existingIndex >= 0) projects[existingIndex] = mergedProject;
  else projects.push(mergedProject);

  const activeExists = projects.some((project) => project.id === state.activeProjectId);
  const cronograma = state.cronograma || { items: [], weeklyChecked: {} };
  const importedSchedule = createPisoSchedule(targetProjectId);

  return {
    ...state,
    projects,
    activeProjectId: activeExists ? state.activeProjectId : targetProjectId,
    cronograma: {
      ...cronograma,
      items: mergeById(cronograma.items || [], [importedSchedule]),
      weeklyChecked: cronograma.weeklyChecked || {}
    }
  };
}
