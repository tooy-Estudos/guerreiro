const mergeById = (current = [], imported = []) => {
  const loose = [];
  const byId = new Map();
  current.forEach((item) => {
    if (item?.id) byId.set(item.id, item);
    else loose.push(item);
  });
  imported.forEach((item) => {
    if (!item?.id) return;
    byId.set(item.id, { ...(byId.get(item.id) || {}), ...item });
  });
  return [...loose, ...Array.from(byId.values())];
};

const mergeNotes = (current = [], imported = []) => {
  const importedIds = new Set(imported.map((item) => item.id));
  const currentWithoutImported = current.filter((item) => !importedIds.has(item?.id));
  return [...imported, ...currentWithoutImported];
};

const normalizeText = (value) => String(value || '')
  .normalize('NFD')
  .replace(/[\u0300-\u036f]/g, '')
  .toLowerCase();

const isLuisPrumoProject = (project = {}) => {
  const name = normalizeText(project.name);
  const description = normalizeText(project.description);
  return project.id === LUIS_PRUMO_PROJECT_ID
    || (name.includes('oab') && name.includes('2026'))
    || (name.includes('oab') && description.includes('prumo'));
};

